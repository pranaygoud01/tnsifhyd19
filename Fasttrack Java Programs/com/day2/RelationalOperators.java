public class RelationalOperators {

	public static void main(String[] args) {
		int a=8,b=8;
		System.out.println(a==b);
		if(a>b) {
			System.out.println(a+" is Greater than "+b);
		}
		else if(b>a) {
			System.out.println(b+" is Greater than "+a);
		}
		else if(b==a) {
			System.out.println(a+" & "+b+"are equal");
		}

	}

}
